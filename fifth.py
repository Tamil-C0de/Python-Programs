# Arithmetic Operators
'''
print(a + b)
print(a - b)
print(a * b)
print(a / b) # Division - float -> Quotient
print(a % b) # Modulus -> Remainder
print(a ** b) # Exponent 10^5
print(a // b) # floor division
'''

# Assignment Operators
'''
a += 5  #a = a + 5
b -= 3  # b = b - 3
a *= 2   # a = a * 2
print(a)
print(b)
'''

# Comparison Operators -> Boolean
'''
x = 12
y = 10
print(x == y)
print(x != y)
print(x >= y)
print(x <= y)
'''

# Logical Operators -> and, or, not -> Boolean
# and -> (L) and (R) -> Both L and R => True(T); False
# or -> (L) or (R) -> In L or R => True(T); Both L and R -> False
# not -> Reverse => T - F; F - T
'''
a = 10
b = 5
print(a < 5 or b > 10)
print(not(a < 5 or b > 10))
'''

# Identity Operator -> variable memory location compare
'''
x = [1, 2, 3]
y = x
z = [1, 2, 3]
print(x is y)
print(x is z)
print(x is not z)
'''

# Membership Operator -> in, not in
'''
list1 = [1, 2, 3, 4, 5]
print(6 in list1)
string = "Hello World"
print('hello' in string)

print(10 not in list1)
print('World' not in string)
'''

